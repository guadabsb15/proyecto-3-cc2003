        if (returnVal == JFileChooser.APPROVE_OPTION) {
            generated = fc.getSelectedFile();  
            try {
                FileBuilder f = new FileBuilder(specification, generated);
                f.writeFile();
                generated.renameTo(new File(generated.getAbsolutePath() + ".java"));
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
            
        }
